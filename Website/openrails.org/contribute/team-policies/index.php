<h2>Content Compatibility</h2>
<p>
Open Rails will continue to maintain compatibility with content from previous official versions of Open Rails.
</p>

<h2>File Formats</h2>
<p>
New types of content will use the JavaScript Object Notation (JSON). 
<br>
Existing types of content from previous official versions of Open Rails will continue to be loaded without change.
</p>

<h2>User Language</h2>
<p>
English documents and text use American spelling (license, color etc.).
</p>

<h2>Keyboard Assignments</h2>
<p>
New key assignments should obey the following rules where that is possible.
<ul>
  <li>Reserve digit keys for camera operations.</li>
  <li>Reserve Fn keys for pop-up windows.</li>
  <li>Reserve the Alt modifier for debug operations.</li>
  <li>Reserve modifier+space (e.g. Ctrl+space) for future expansion using sequences of keys.</li>
</ul>
Existing key assignments may not be changed without community discussion and approval of the Open Rails Management Team.
</p>

<h2>Crashes and Derailments</h2>
<p>
Open Rails simulates emergency events which bring a train to a standstill, such as a derailment, and provides both a visual indication 
and a notification of the problem.
</p>
<p>
Open Rails does not show rolling stock leaving the track due to crashes or derailments. 
We have many members from the rail industry who have spent their working lives preventing incidents that cause injuries and loss of life 
and we respect that point of view.
</p>

